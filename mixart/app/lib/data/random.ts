type CountryCity = {
    city: string;
    postalCodes: string[];
};

type CountryCities = {
    [key: string]: CountryCity[];
};

function getRandomElement<T>(array: T[]): T {
    return array[Math.floor(Math.random() * array.length)];
}

function getRandomDateOfBirth(): string {
    const start = new Date(1950, 0, 1);
    const end = new Date(2003, 0, 1);
    const date = new Date(start.getTime() + Math.random() * (end.getTime() - start.getTime()));
    return date.toISOString().split('T')[0];
}

function generateRandomEmail(): string {
    const surname = getRandomElement(randomSurnames);
    const randomNumber = Math.floor(Math.random() * 99999) + 1;
    return `${surname}${randomNumber}@gmail.com`;
}

export function generateRandomCustomerAndAddress() {
    const countryCode = getRandomElement(Object.keys(countryCities)) as keyof typeof countryCities;
    const cityData = getRandomElement(countryCities[countryCode]);
    const city = cityData.city;
    const postalCode = getRandomElement(cityData.postalCodes);

    const addressLine1 = `${Math.floor(Math.random() * 1000)} ${getRandomElement(randomStreetNames)}`;
    const addressLine2 = `${getRandomElement(randomAddressLine2)} ${Math.floor(Math.random() * 500)}`;

    const firstName = getRandomElement(randomFirstNames);
    const lastName = getRandomElement(randomSurnames);

    const email = generateRandomEmail();

    const customer = {
        referenceId: `cust-${Math.floor(Math.random() * 100000)}`,
        citizenshipCountryCode: countryCode,
        firstName: firstName,
        lastName: lastName,
        dateOfBirth: getRandomDateOfBirth(),
        locale: 'en',
        routingGroup: 'trusted',
        email: email
    };

    const billingAddress = {
        addressLine1: addressLine1,
        addressLine2: addressLine2,
        city: city,
        countryCode: countryCode,
        postalCode: postalCode,
    };

    return { customer, billingAddress };
}